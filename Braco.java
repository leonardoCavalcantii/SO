public class Braco {

    int posTrilha;
    int posBloco;


    public Braco() {
        this.posTrilha = 0;
        this.posBloco = 0;
    }

    public int getPosTrilha() {
        return this.posTrilha;
    }

    public int getPosBloco() {
        return this.posBloco;
    }

    public void setPosTrilha(int posicao) {
        this.posTrilha = posicao;
    }

    public void setPosBloco(int posicao) {
        this.posBloco = posicao;
    }
}
